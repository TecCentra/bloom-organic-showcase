import { defineConfig } from "vite";
import react from "@vitejs/plugin-react-swc";
import path from "path";
import { componentTagger } from "lovable-tagger";

// https://vitejs.dev/config/
export default defineConfig(({ mode }) => ({
  server: {
    host: "::",
    port: 8080,
  },
  plugins: [
    // Only enable React Fast Refresh in development mode
    mode === "development" && react(),
    // Enable lovable-tagger only in development mode as well
    mode === "development" && componentTagger(),
  ].filter(Boolean),
  resolve: {
    alias: {
      "@": path.resolve(__dirname, "./src"),
    },
  },
  build: {
    rollupOptions: {
      // Prevent Rollup from trying to resolve @react-refresh in production
      external: ["/@react-refresh"],
    },
  },
}));
